class vehicle:
    def __init__ (self,max_speed,mileage):
        self.max_speed=max_speed
        self.mileage= mileage
audi=vehicle(240,19)
print("audi max speed:",audi.max_speed)
print("audi mileage:",audi.mileage)
mercedes=vehicle(300,20)
print("mercedes max speed:",mercedes.max_speed)
print("mercedes mileage:",mercedes.mileage)
    